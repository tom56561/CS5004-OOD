/**
 * The Term interface defines all the supported operations for each term.
 * 
 * @author eddie
 *
 */
public interface TermInterface {

  /**
   * Takes a double-precision decimal number and returns a double-precision
   * result.
   * 
   * @param x the double-precision decimal number
   * @return a double-precision result
   */
  double evaluate(double x);
}
